import turtle

board = turtle.Turtle()

board.forward(100)  # draw base

board.left(120)
board.forward(100)

board.left(120)
board.forward(100)

board = turtle.Turtle()
board.forward(150) # moving the turtle Forward by 150 units
board.left(90) #Turning the turtle by 90 degrees
board.forward(150)
board.left(90)
board.forward(150)
board.left(90)
board.forward(150)
board.left(90)


turtle.done()